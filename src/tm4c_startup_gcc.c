/* Passess the stack pointer into the hardfault function, see 
 * https://interrupt.memfault.com/blog/cortex-m-fault-debug */
#define HARDFAULT_HANDLING_ASM(_x)               \
  __asm volatile(                                \
      "tst lr, #4 \n"                            \
      "ite eq \n"                                \
      "mrseq r0, msp \n"                         \
      "mrsne r0, psp \n"                         \
      "b debug_fault_handler \n"                 \
)

